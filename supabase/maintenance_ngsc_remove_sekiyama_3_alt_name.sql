-- =========================================================================
-- 【専用・任意】NGSC から「関山」を外す — ③ 名前が「関山」以外の表記のときだけ
-- =========================================================================
-- ① preview に行が出てこないのに画面上に関山が残る場合などに、
--    スペース付き「関山 」などで保存されている可能性があります。
-- 通常は不要。② のあとでまだ残るときだけ確認用に使う。
-- =========================================================================

UPDATE schedule_entries
SET workers = array_remove(workers, '関山 ')
WHERE date >= '2026-05-01'
  AND regexp_replace(koujimei, ' [A-Z]$', '') = 'NGSC'
  AND workers @> ARRAY['関山 ']::text[]
RETURNING id, date, koujimei, shift, workers;
