-- =========================================================================
-- 【専用】NGSC から「関山」を外す — ① 確認だけ（データは変えない）
-- =========================================================================
-- 手順: Supabase → SQL Editor → このファイルの中身をそのまま貼って「実行」
-- 次に: 表示された行が正しければ ② のファイルを実行する
--
-- 対象: 日付が「開始日」以降、工事名のベースが NGSC、workers に関山を含む行
-- 開始日を変えるときは、下の日付リテラルを ② と同じ値に直す（例: '2025-05-01'）
-- =========================================================================

SELECT id, date, koujimei, shift, workers
FROM schedule_entries
WHERE date >= '2026-05-01'
  AND regexp_replace(koujimei, ' [A-Z]$', '') = 'NGSC'
  AND workers @> ARRAY['関山']::text[]
ORDER BY date, koujimei, shift;
